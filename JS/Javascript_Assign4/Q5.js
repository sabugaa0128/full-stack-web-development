//Ascending

let numAsc = 1;
while (numAsc <= 25) {
    console.log(numAsc);
    numAsc++;
}

//Descending
let numDesc = 25;
while (numDesc >= 1) {
    console.log(numDesc);
    numDesc--;
}

//Both cases

let num = 1;

// Ascending order
console.log("Ascending order:");
while (num <= 25) {
    console.log(num);
    num++;
}

// Descending order
console.log("Descending order:");
num = 25;
while (num >= 1) {
    console.log(num);
    num--;
}
